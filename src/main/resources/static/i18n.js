  function normalizeLocale(locale) {
    if (locale === "zh") return "zh-CN";
    return locales.includes(locale) ? locale : "ko";
  }

  function readLocale() {
    const saved = localStorage.getItem(storageKey) || localStorage.getItem("archive-logistics-language");
    return normalizeLocale(saved);
  }

  function translate(locale, key, params = {}) {
    const normalized = normalizeLocale(locale);
    const template = messages[normalized]?.[key] ?? messages.ko[key] ?? key;
    return Object.entries(params).reduce((value, [name, replacement]) => {
      return value.replaceAll(`{${name}}`, String(replacement));
    }, template);
  }

  function missingKeys() {
    const baseKeys = Object.keys(messages.ko);
    return Object.fromEntries(locales.map((locale) => [
      locale,
      baseKeys.filter((key) => !(key in messages[locale])),
    ]));
  }

  return {
    locales,
    localeNames,
    storageKey,
    messages,
    normalizeLocale,
    readLocale,
    t: translate,
    missingKeys,
  };
